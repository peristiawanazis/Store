    <div class="about-title">Frequently Asked Questions</div>
    <div class="about-subtitle">Apa itu Store.Telkomspeedy.com</div>
    <div class="about-content">
        Store.Telkomspeedy.com merupakan portal yang berisi layanan content dan aplikasi yang dapat digunakan oleh pengguna
        secara online atau offline dengan melakukan pembelian terlebih dahulu menggunakan metode pembayaran Speedy.
        <br /><br />
        Tagihan layanan content dan aplikasi yang Anda beli akan ditagihkan menjadi satu tagihan bulanan Speedy Anda,
        sehingga memudahkan Anda dalam melakukan pembayaran layanan yang digunakan.
    </div>
    <div class="about-subtitle">Apakah Pembelian Content Dijamin Keamanannya</div>
    <div class="about-content">
        Proses pembelian ini dijamin keamanannya dikarenakan hanya dapat dilakukan dari rumah atau jaringan Speedy Anda,
        sehingga dipastikan proses transaksi hanya dilakukan oleh orang-orang yang tahu nomor Speedy, Telepon Rumah dan dilakukan dari rumah Anda.
    </div>
    <div class="about-subtitle">Apakah yang dimaksud layanan Content dan Content Provider</div>
    <div class="about-content">
        Layanan content adalah karya dijital yang dapat dimanfaatkan oleh pengguna untuk maksud tertentu baik untuk edukasi
        atau entertainment seperti aplikasi, simulai pembelajaran, games, musik, video atau lainnya.
        Layanan content ini disediakan oleh mitra dari Telkom yang dikenal sebagai Content Provider.
    </div>
    <div class="about-subtitle">Bagaimana Cara Pembelian Content</div>
    <div class="about-content">
        Untuk melakukan pembelian content, pengguna dapat login terlebih dahulu kemudian memilih content yang akan dibeli,
        untuk pembelian content dengan menggunakan metode pembayaran yang ditagihkan di Speedy, 
        transaksi pembelian harus dilakukan dari jaringan Speedy pelanggan atau dalam rumah Pelanggan.
    </div>
    <div class="about-subtitle">Bagaimana Tagihan Pembelian Content Dibayarkan oleh Pelanggan</div>
    <div class="about-content">
        Untuk pembayaran layanan content yang dibeli melalui pembayaran dengan tagihan di Speedy,
        maka pembayaran content dilakukan bersama-sama dengan pembayaran tagihan Speedy melalui ATM atau Plasa Telkom.
        <br /><br />
        Jenis pembayaran untuk setiap content ada dua model, Berlangganan dimana tagihan dilakukan
        setiap bulan dan Sekali bayar dimana biaya hanya ditagihkan sekali pada saat terjadi transaksi.
    </div>
    <div class="about-subtitle">Bagaimana Cara Berhenti Berlanggan Content</div>
    <div class="about-content">
        Untuk berhenti berlangganan, dapat dilakukan dengan login terlebih dahulu dan klik menu My Content,
        untuk layanan yang tengah digunakan maka terdapat menu berhenti, klik menu berhenti untuk berhenti berlangganan.
    </div>
	<div class="about-subtitle">Kontak Kami</div>
    <div class="about-content">
        Jika Anda memiliki keluhan terhadap layanan ini, Anda dapat menyampaikannya melalui Plasa Telkom terdekat di kota Anda atau 
		hubungi Call Center Telkom via telepon ke 147 (pilih layanan "Delima dan Konten").
    </div>
